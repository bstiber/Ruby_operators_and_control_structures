# using elsif statement, modify the code below to check whether or not the
# car_speed is equal to the speed_limit.   If it is, set the variable
# "going_speed_limit" to true.

car_speed = 55
speed_limit = 55
going_speed_limit = true

if car_speed == speed_limit
  going_speed_limit == true
elsif car_speed < speed_limit
  going_speed_limit = false
else
  going_speed_limit = false
end
